package org.fasttrackit;

public class FoodAnimal {

    String name;
    int price;
    int cantity;
    String expirationDate;

    public FoodAnimal(String name, int price, int cantity, String expirationDate) {
        this.name = name;
        this.price = price;
        this.cantity = cantity;
        this.expirationDate = expirationDate;
    }
}
