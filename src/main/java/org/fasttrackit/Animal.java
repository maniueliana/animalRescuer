package org.fasttrackit;

public class Animal {

    String name;
    int age;
    int health;
    int happy;
    String foodPrefered;
    String hobby;

    public Animal(String name, int age, int health, int happy, String foodPrefered, String hobby) {
        this.name = name;
        this.age = age;
        this.health = health;
        this.happy = happy;
        this.foodPrefered = foodPrefered;
        this.hobby = hobby;
    }
}
